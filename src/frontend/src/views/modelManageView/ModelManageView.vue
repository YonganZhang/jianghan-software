function handleParamView(record: Record<string, any>) {
  paramRecord.value = record
  paramModalOpen.value = true
  paramLoading.value = true
  paramRows.value = []
  getParameters({ id: record.id, model_type: record.model_type })
    .then((res) => {
      if (res?.code !== 200) {
        message.error(res?.message || '参数获取失败')
        return
      }
      const data = res?.data || {}
      paramRows.value = Object.entries(data).map(([name, value]) => {
        let text = ''
        if (value === null || value === undefined) text = ''
        else if (typeof value === 'string') text = value
        else if (typeof value === 'number' || typeof value === 'boolean') text = String(value)
        else text = JSON.stringify(value)
        return { key: name, name, value: text }
      })
    })
    .catch((e: any) => {
      message.error(e?.message || '参数获取失败')
    })
    .finally(() => {
      paramLoading.value = false
    })
}
